package mkcloudadmin.model.mkcloud.paging;

public class FinanceMembersManage {

    private Long userId;
    private String platformName;
    private String channelName;
    private String realName;
    private String mobileNum;
    private String accountNo;
    private String inviteName;
    private String inviteMobileNum;

    public Long getUserId() {
        return userId;
    }

    public void setUserId(Long userId) {
        this.userId = userId;
    }

    public String getPlatformName() {
        return platformName;
    }

    public void setPlatformName(String platformName) {
        this.platformName = platformName;
    }

    public String getChannelName() {
        return channelName;
    }

    public void setChannelName(String channelName) {
        this.channelName = channelName;
    }

    public String getRealName() {
        return realName;
    }

    public void setRealName(String realName) {
        this.realName = realName;
    }

    public String getMobileNum() {
        return mobileNum;
    }

    public void setMobileNum(String mobileNum) {
        this.mobileNum = mobileNum;
    }

    public String getAccountNo() {
        return accountNo;
    }

    public void setAccountNo(String accountNo) {
        this.accountNo = accountNo;
    }

    public String getInviteName() {
        return inviteName;
    }

    public void setInviteName(String inviteName) {
        this.inviteName = inviteName;
    }

    public String getInviteMobileNum() {
        return inviteMobileNum;
    }

    public void setInviteMobileNum(String inviteMobileNum) {
        this.inviteMobileNum = inviteMobileNum;
    }
}
